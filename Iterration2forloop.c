#include<stdio.h>

int main()  //Function with the for loop i.e. Iteration
{
   int iCnt = 0; // Loop Counter
   //    0=initialisation  1=condition    2=displacement
   for(iCnt = 1;         iCnt <= 11;       iCnt++)
   {
     printf("Jay Ganesh...\n");  // <- 4=printf loop body.
   };


  return 0;
}
